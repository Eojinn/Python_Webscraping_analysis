# 1. 합계를 저장할 변수 초기화
total = 0

# 2. while 루프를 사용하여 무한 반복
while True:
    # 사용자로부터 숫자 입력 받기
    num = int(input("숫자를 입력하세요 (0을 입력하면 종료): "))
    
    # 3. 종료 조건 확인
    # 입력받은 숫자가 0이면 루프를 중단
    if num == 0:
        break
    
    # 입력받은 숫자를 합계에 더하기
    total += num

# 4. 최종 결과 출력
print(f"입력된 숫자들의 합: {total}")
