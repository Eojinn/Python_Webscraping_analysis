# 1. 학생 정보를 담은 딕셔너리의 리스트 생성
students = [
    {"이름": "김철수", "나이": 20, "학과": "컴퓨터공학과"},
    {"이름": "이영희", "나이": 22, "학과": "영어영문학과"},
    {"이름": "박민수", "나이": 21, "학과": "경영학과"},
    {"이름": "최수진", "나이": 23, "학과": "수학과"}
]

# 2. 나이(age)를 기준으로 리스트 정렬
# lambda를 사용하여 딕셔너리의 '나이' 값을 기준으로 정렬하도록 지정
students.sort(key=lambda x: x["나이"])

# 3. 결과 출력
print("나이 순으로 정렬된 학생 목록:")
for s in students:
    print(f"{s['이름']} ({s['나이']}세) - {s['학과']}")